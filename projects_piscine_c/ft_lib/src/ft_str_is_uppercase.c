int	ft_str_is_uppercase(char *str)
{
	int i;

	i = 0;
	while (str[i] != '\0')
	{
		if (str[0] == '\0')
			return (1);
		if (str[i] > 'Z' || str[i] < 'A')
			return (0);
		i++;
	}
	return (1);
}
